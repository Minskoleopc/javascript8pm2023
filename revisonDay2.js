// Logical operator
// && - AND 

// true     &&     true    ======> true 
// false    &&     true    =======> false 
// true     &&     false   =======> false 
// false    &&     false   =======> false 

console.log(7 == 7 && 8 == 8)
console.log(7 != 7 && 8 == 8)
console.log(7 == 7 && 8 != 8)
console.log(7 != 7 && 8 != 8)


// Or -- 

// true  ||   true     =======> true 
// false ||   true     =======> true 
// true  ||   false    =======> true 
// false ||   false    =======> false


console.log(7 == 7 || 8 == 8) // true
console.log(7 != 7 || 8 == 8) // true
console.log(7 == 7 || 8 != 8) // true 
console.log(7 != 7 || 8 != 8) // false

// Not
// True     ====> !    False 
// False    ====> !    True

